// ─── Matrix Figure Sprites ──────────────────────────────────────────────────
// Density levels: '#' = 1.0 (core), '+' = 0.7 (limbs), '.' = 0.4 (edges), ' ' = 0 (empty)
// Each sprite: 36 chars wide × 48 rows tall

export interface FigurePose {
  width: number;
  height: number;
  rows: string[];
}

export interface CharacterSprites {
  label: string;
  stand: FigurePose;
  walkRight: FigurePose[]; // 6 frames
  special: FigurePose;
}

export function mirrorPose(pose: FigurePose): FigurePose {
  return {
    width: pose.width,
    height: pose.height,
    rows: pose.rows.map((row) => row.split("").reverse().join("")),
  };
}

function pose(rows: string[]): FigurePose {
  return { width: 36, height: rows.length, rows };
}

